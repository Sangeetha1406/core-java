class Soil{
		static String soilName;
		static String soilType;
		static String soilColor;
		
		static void setSoilName(String sm)
		{
			soilName=sm;
		}
		static String getSoilName()
		{
			return soilName;
		}
		static void setSoilType(String tp)
		{
			soilType=tp;
		}
		static String getSoilType()
		{
			return soilType;
		}
		static void setSoilColor(String sclr)
		{
			soilColor=sclr;
		}
		static String getSoilColor()
		{
			return soilColor;
		}


}

