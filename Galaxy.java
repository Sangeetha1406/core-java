class Galaxy{
		static String gName;
		static String gType;
		static String numberOfStars;
		
		static void setGName(String gn)
		{
			gName=gn;
		}
		static String getGName()
		{
			return gName;
		}
		static void setGType(String typ)
		{
			gType=typ;
		}
		static String getGType()
		{
			return gType;
		}
		static void setNumberOfStars(String nos)
		{
			numberOfStars=nos;
		}
		static String getNumberOfStars()
		{
			return numberOfStars;
		}


}

