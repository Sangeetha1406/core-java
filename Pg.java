class Pg{
		static String pgName;
		static String pgType;
		static String place;
		
		static void setPgName(String pgn)
		{
			pgName=pgn;
		}
		static String getPgName()
		{
			return pgName;
		}
		static void setPgType(String pgtyp)
		{
			pgType=pgtyp;
		}
		static String getPgType()
		{
			return pgType;
		}
		static void setPlace(String plc)
		{
			place=plc;
		}
		static String getPlace()
		{
			return place;
		}


}

