class RailwayStation{
		static String rName;
		static String rTrain;
		static String rTimings;
		
		static void setRName(String rn)
		{
			rName=rn;
		}
		static String getRName()
		{
			return rName;
		}
		static void setRTrain(String tr)
		{
			rTrain=tr;
		}
		static String getRTrain()
		{
			return rTrain;
		}
		static void setRTimings(String tm)
		{
			rTimings=tm;
		}
		static String getRTimings()
		{
			return rTimings;
		}


}

