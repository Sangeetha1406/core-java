class Comparsion{
public static void main(String args[]){
/* comparsion operator */
int a = 20;
int b = 30;

System.out.println(a<b); //true
System.out.println(a==50);
System.out.println(a>b);

System.out.println("----------");

System.out.println(a>=b);
System.out.println(a==b);

System.out.println("-----------");

System.out.println(a!=b);
System.out.println(b==60);
}

}