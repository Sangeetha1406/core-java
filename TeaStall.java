class TeaStall{
		static int teaStallId;
		static String item;
		static String adress;
		static long contactNo;
		
		static void setTeaStallId(int id)
		{
			teaStallId=id;
		}
		static int getTeaStallId()
		{
			return teaStallId;
		}
		static void setItem(String itm)
		{
			item=itm;
		}
		static String getItem()
		{
			return item;
		}
		static void setAdress(String adrs)
		{
			adress=adrs;
		}
		static String getAdress()
		{
			return adress; 
		}
		static void setContactNo(long cno)
		{
			contactNo=cno;
		}
		static long getContactNo()
		{
			return contactNo;
		}
}