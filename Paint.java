class Paint{
		static int paintId;
		static String brandName;
		static String color;
		
		static void setPaintId(int pid)
		{
			paintId=pid;
		}
		static int getPaintId()
		{
			return paintId;
		}
		static void setBrandName(String bnm)
		{
			brandName=bnm;
		}
		static String getBrandName()
		{
			return brandName;
		}
		static void setColor(String clr)
		{
			color=clr;
		}
		static String getColor()
		{
			return color;
		}


}

